// РЕКУРСИЯ
// когда ф-ция вызывает сама себя
/*

Факториал рекурсивно
Напишите рекурсивную функцию factorial, которая принимает целое число и возвращает факториал числа.


*/

#include <iostream>
using namespace std;

int factorial(int n)
{
    if (n == 0)
    {
        return 1; // базовый случай: факториал 0 равен 1
    }
    else
    {
        return n * factorial(n - 1); // рекурсивный вызов
    }
}