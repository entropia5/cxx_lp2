#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "Russian"); // Чтобы консоль понимала русский язык

    // 1. Создаем динамическое число в куче
    int *x = new int;
    *x = 42;

    // 2. Создаем ссылку (псевдоним) на то, что лежит по адресу x
    // Мы говорим: "y — это второе имя для той ячейки, на которую смотрит x"
    int &y = *x;

    cout << "Указатель" << endl;
    cout << "Адрес самого указателя (&x):   " << &x << " (Стек)" << endl;
    cout << "Значение внутри x (адрес):     " << x  << " (Куча)" << endl;
    cout << "То, на что смотрит x (*x):     " << *x << endl;

    cout << "Ссылка y " << endl;
    cout << "Адрес ссылки (&y):             " << &y << " (Тот же адрес в куче!)" << endl;
    cout << "Значение ссылки (y):           " << y  << endl;
    // cout << *y; // ОШИБКА! Попытка разыменовать обычное число (int).

    cout << "\n--- Работа через ссылку ---" << endl;
    y = 100; // Меняем значение через "второе имя"
    cout << "Новое значение через *x:       " << *x << " (Тоже изменилось!)" << endl;

    // 3. Освобождение памяти
    delete x;
    // Память в куче удалена. Теперь и x, и y "смотрят" на мусор.

    return 0;
}
